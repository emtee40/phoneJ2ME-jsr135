package com.sun.mmedia.rtsp.sdp;

public class MediaAttribute {
    private String name;
    private String value;

    public MediaAttribute(String name, String value) {
        this.name = name;
        this.value = value;
    }

    public String getName() {
        return name;
    }

    public String getValue() {
        return value;
    }
}
